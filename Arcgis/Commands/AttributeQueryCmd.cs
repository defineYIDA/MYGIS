        private IHookHelper m_hookHelper = null;
        public AttributeQueryCmd()
        {
            base.m_category = "WindowsFormsApplication1";
            base.m_caption = "属性条件查询";
            base.m_message = "属性条件查询";
            base.m_toolTip = "属性条件查询";
            base.m_name = "AttributeQueryCmd";
            try
            {
                string bitmapResourceName = GetType().Name + ".bmp";
                base.m_bitmap = new Bitmap(GetType(), bitmapResourceName);
            }
            catch (Exception ex)
            {
                System.Diagnostics.Trace.WriteLine(ex.Message, "Invalid Bitmap");
            }
        }

        #region Overridden Class Methods

        /// <summary>
        /// Occurs when this command is created
        /// </summary>
        /// <param name="hook">Instance of the application</param>
        public override void OnCreate(object hook)
        {
            if (hook == null)
                return;

            try
            {
                m_hookHelper = new HookHelperClass();
                m_hookHelper.Hook = hook;
                if (m_hookHelper.ActiveView == null)
                    m_hookHelper = null;
            }
            catch
            {
                m_hookHelper = null;
            }

            if (m_hookHelper == null)
                base.m_enabled = false;
            else
                base.m_enabled = true;

            // TODO:  Add other initialization code
        }

        /// <summary>
        /// Occurs when this command is clicked
        /// </summary>
        public override void OnClick()
        {
            if (m_hookHelper.Hook == null) return;
            if (m_hookHelper.FocusMap.LayerCount != 0)
            {
                SelectByAttribute frmAttributeQuery = new SelectByAttribute(m_hookHelper);
                frmAttributeQuery.Show(m_hookHelper as System.Windows.Forms.IWin32Window);

            }
        }

        #endregion
    }
